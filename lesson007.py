# Create a program that calculates the force given the mass and the
# acceleration, by using the formula: F = m*a.
# Then, print:
# "The mass is X, the acceleration is Y, and the force is Z"

mass = float(input("Please enter the mass: "))
accel = float(input("Please enter the acceleration: "))
force = mass * accel
print(f"The mass is {mass}, the acceleration is {accel}, and the force is {force}")

